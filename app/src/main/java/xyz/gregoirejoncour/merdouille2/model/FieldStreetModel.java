public class FieldStreetModel {

    private String histo;
    private String texte;
    private String typvoie;
    private String arron;
    private String typo;

    public String getHisto() {
        return histo;
    }

    public void setHisto(String histo) {
        this.histo = histo;
    }

    public String getTexte() {
        return texte;
    }

    public void setTexte(String texte) {
        this.texte = texte;
    }

    public String getTypvoie() {
        return typvoie;
    }

    public void setTypvoie(String typvoie) {
        this.typvoie = typvoie;
    }

    public String getArron() {
        return arron;
    }

    public void setArron(String arron) {
        this.arron = arron;
    }

    public String getTypo() {
        return typo;
    }

    public void setTypo(String typo) {
        this.typo = typo;
    }
}
